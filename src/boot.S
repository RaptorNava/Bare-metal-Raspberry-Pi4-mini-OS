/**
 * boot.S — Точка входа в операционную систему
 *
 * Этот файл — первый код, который выполняется при запуске.
 * Raspberry Pi 4 запускает все 4 ядра, но нам нужно только одно (ядро 0).
 * Остальные ядра (1, 2, 3) мы «усыпляем» в бесконечном цикле WFE.
 *
 * Порядок загрузки RPi4:
 *   1. GPU читает config.txt и загружает kernel8.img в 0x80000
 *   2. Все 4 ядра стартуют с адреса 0x80000
 *   3. Наш код определяет, какое ядро активно (MPIDR_EL1)
 *   4. Только ядро 0 продолжает работу
 *   5. Настраиваем стек и прыгаем в kernel_main()
 *
 * Используется синтаксис GNU AS для AArch64.
 */

.section ".text.boot"   // Специальная секция — линкер поставит её первой
.global _start          // Экспортируем символ _start как точку входа

_start:
    // ---------------------------------------------------------------
    // Шаг 1: Определить номер текущего ядра
    // MPIDR_EL1 — регистр идентификации ядра в AArch64
    // Биты [1:0] содержат Aff0 — номер ядра (0, 1, 2, 3)
    // ---------------------------------------------------------------
    mrs     x0, mpidr_el1       // Читаем системный регистр MPIDR_EL1 в x0
    and     x0, x0, #0xFF       // Маскируем биты [7:0] (Aff0 = номер ядра)
    cbz     x0, core0_start     // Если ядро 0 — прыгаем к инициализации
    
    // ---------------------------------------------------------------
    // Шаг 2: Ядра 1, 2, 3 — уходим в сон (WFE = Wait For Event)
    // WFE снижает энергопотребление, SEV разбудит их если понадобится
    // ---------------------------------------------------------------
secondary_cores_sleep:
    wfe                         // Спим до события (энергосберегающий режим)
    b       secondary_cores_sleep  // Проснулись — снова спим

    // ---------------------------------------------------------------
    // Шаг 3: Инициализация ядра 0
    // ---------------------------------------------------------------
core0_start:
    // Настройка EL (Exception Level)
    // RPi4 стартует в EL2 (гипервизор), нам нужен EL1 (ядро ОС)
    mrs     x0, CurrentEL       // Читаем текущий уровень привилегий
    lsr     x0, x0, #2          // Сдвигаем биты CurrentEL[3:2]
    cmp     x0, #2              // Проверяем EL2
    beq     switch_to_el1       // Если EL2 — переключаемся в EL1
    b       setup_stack         // Если уже EL1 — идём к стеку

switch_to_el1:
    // ---------------------------------------------------------------
    // Переключение EL2 -> EL1
    // Настраиваем HCR_EL2 (Hypervisor Configuration Register)
    // RW бит [31] = 1: EL1 работает в AArch64 режиме
    // ---------------------------------------------------------------
    mov     x0, #(1 << 31)      // RW = 1: EL1 использует AArch64
    msr     hcr_el2, x0         // Пишем в регистр гипервизора
    
    // Настройка SPSR_EL2 для возврата в EL1
    // D[9]=1 Debug, A[8]=1 SError, I[7]=1 IRQ, F[6]=1 FIQ маски
    // M[3:0]=0101 = EL1h (использовать SP_EL1)
    mov     x0, #0x3C5          // DAIF маски + EL1h режим
    msr     spsr_el2, x0        // Сохраняем состояние для eret
    
    // Устанавливаем адрес возврата — продолжим в setup_stack
    adr     x0, setup_stack     // Адрес метки setup_stack
    msr     elr_el2, x0         // Exception Link Register EL2
    eret                        // Возврат из "исключения" -> переходим в EL1

setup_stack:
    // ---------------------------------------------------------------
    // Шаг 4: Настройка стека
    // Стек растёт вниз (от высоких адресов к низким)
    // Начало ядра: 0x80000, стек ставим ПЕРЕД кодом
    // ---------------------------------------------------------------
    ldr     x0, =_start         // Загружаем адрес начала кода (0x80000)
    mov     sp, x0              // SP = 0x80000 (стек растёт вниз от этой точки)

    // ---------------------------------------------------------------
    // Шаг 5: Очистка секции BSS
    // BSS содержит неинициализированные глобальные переменные
    // Стандарт C требует их обнуления перед вызовом main()
    // ---------------------------------------------------------------
    ldr     x0, =__bss_start    // Начало BSS (символ из linker.ld)
    ldr     x1, =__bss_end      // Конец BSS (символ из linker.ld)
    
bss_clear_loop:
    cmp     x0, x1              // Достигли конца BSS?
    bge     bss_done            // Да — выходим
    str     xzr, [x0], #8      // Записываем 0 и сдвигаем указатель на 8 байт
    b       bss_clear_loop      // Продолжаем

bss_done:
    // ---------------------------------------------------------------
    // Шаг 6: Вызов главной функции ядра
    // kernel_main() определена в src/kernel/kernel.c
    // ---------------------------------------------------------------
    bl      kernel_main         // Branch with Link — вызываем kernel_main()
    
    // ---------------------------------------------------------------
    // Шаг 7: Если kernel_main() вернулась (не должна!) — зависаем
    // ---------------------------------------------------------------
halt:
    wfe                         // Энергосберегающий режим
    b       halt                // Бесконечный цикл

// ---------------------------------------------------------------
// Векторная таблица прерываний (упрощённая)
// В реальной ОС каждый вектор обрабатывает своё исключение
// Размер каждого вектора — 0x80 байт (128 байт), выравнивание 0x800
// ---------------------------------------------------------------
.balign 0x800                   // Выравнивание на 2048 байт (требование AArch64)
.global vectors
vectors:
    // EL1t — текущий EL, использует SP_EL0
    b       irq_handler_stub    // 0x000: Synchronous
    .balign 0x80
    b       irq_handler_stub    // 0x080: IRQ
    .balign 0x80
    b       irq_handler_stub    // 0x100: FIQ  
    .balign 0x80
    b       irq_handler_stub    // 0x180: SError
    
    // EL1h — текущий EL, использует SP_EL1
    .balign 0x80
    b       sync_handler_stub   // 0x200: Synchronous
    .balign 0x80
    b       irq_entry           // 0x280: IRQ — наш главный обработчик
    .balign 0x80
    b       irq_handler_stub    // 0x300: FIQ
    .balign 0x80
    b       irq_handler_stub    // 0x380: SError

    // EL0 (64-bit)
    .balign 0x80
    b       irq_handler_stub    // 0x400: Synchronous
    .balign 0x80
    b       irq_handler_stub    // 0x480: IRQ
    .balign 0x80
    b       irq_handler_stub    // 0x500: FIQ
    .balign 0x80
    b       irq_handler_stub    // 0x580: SError

// ---------------------------------------------------------------
// Точка входа в обработчик IRQ
// Сохраняем контекст (все регистры) на стек
// ---------------------------------------------------------------
irq_entry:
    // Сохраняем пары регистров на стек (stp = Store Pair)
    sub     sp, sp, #256        // Резервируем место на стеке
    stp     x0,  x1,  [sp, #0]
    stp     x2,  x3,  [sp, #16]
    stp     x4,  x5,  [sp, #32]
    stp     x6,  x7,  [sp, #48]
    stp     x8,  x9,  [sp, #64]
    stp     x10, x11, [sp, #80]
    stp     x12, x13, [sp, #96]
    stp     x14, x15, [sp, #112]
    stp     x16, x17, [sp, #128]
    stp     x18, x19, [sp, #144]
    stp     x20, x21, [sp, #160]
    stp     x22, x23, [sp, #176]
    stp     x24, x25, [sp, #192]
    stp     x26, x27, [sp, #208]
    stp     x28, x29, [sp, #224]
    str     x30,      [sp, #240]  // Link Register (адрес возврата)
    
    bl      handle_irq          // Вызываем C-обработчик из timer.c
    
    // Восстанавливаем регистры
    ldp     x0,  x1,  [sp, #0]
    ldp     x2,  x3,  [sp, #16]
    ldp     x4,  x5,  [sp, #32]
    ldp     x6,  x7,  [sp, #48]
    ldp     x8,  x9,  [sp, #64]
    ldp     x10, x11, [sp, #80]
    ldp     x12, x13, [sp, #96]
    ldp     x14, x15, [sp, #112]
    ldp     x16, x17, [sp, #128]
    ldp     x18, x19, [sp, #144]
    ldp     x20, x21, [sp, #160]
    ldp     x22, x23, [sp, #176]
    ldp     x24, x25, [sp, #192]
    ldp     x26, x27, [sp, #208]
    ldp     x28, x29, [sp, #224]
    ldr     x30,      [sp, #240]
    add     sp, sp, #256
    eret                        // Возврат из исключения

// Заглушки для необработанных исключений
irq_handler_stub:
    b       irq_handler_stub    // Зависаем (для отладки)

sync_handler_stub:
    b       sync_handler_stub
